class BasicsDemo {
	static void print() {
		System.out.println("\n\nInside print ...");
		System.out.println("Hellow, world!!");
		System.out.println();
		System.out.println("Hello, World!!");
		System.out.println("Hello,");
		System.out.println(" ");
		System.out.println("world!!");
	}
	
	public static void main(String[] args) {
		print();
	}

}

